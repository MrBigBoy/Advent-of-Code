        static int TestProcess(List<List<int>> data)
        {
            int safeLevels = 0;

            foreach (var report in data)
            {
                bool isStrictlyIncreasing = true;
                bool isStrictlyDecreasing = true;

                for (int i = 0; i < report.Count - 1; i++)
                {
                    int diff = report[i + 1] - report[i];

                    // Check if the difference is within the valid range
                    if (Math.Abs(diff) is < 1 or > 3)
                    {
                        isStrictlyIncreasing = false;
                        isStrictlyDecreasing = false;
                        break;
                    }

                    // Check strictly increasing or decreasing
                    if (diff > 0) // It's increasing
                    {
                        isStrictlyDecreasing = false;
                    }
                    else if (diff < 0) // It's decreasing
                    {
                        isStrictlyIncreasing = false;
                    }
                }

                // A report is safe if it's either strictly increasing or strictly decreasing
                if (isStrictlyIncreasing || isStrictlyDecreasing)
                {
                    safeLevels++;
                }
            }

            return safeLevels;
        }
